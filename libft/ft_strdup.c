#include "libft.h"

char	*ft_strdup(const char *s)
{
	unsigned int	i;
	char			*dup;
	size_t			len;

	len = ft_strlen(s);
	dup = malloc(len * sizeof(char) + 1);
	i = 0;
	if (!dup)
		return (NULL);
	while (i < len)
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
